# Return

print('==========Return==========\n')

# Return dengan ekspresi
def penjumlahan(x, y):
    return x + y

# Return tidak dengan ekspresi
def perkalian(x, y):
    return

# Memanggil fungsi yang memiliki pernyataan return dengan ekspresi
hasil_penjumlahan = penjumlahan(5, 5)

# Menampilkan hasil eksekusi fungsi
print(hasil_penjumlahan)

# Mengolah dan menampilkan hasil eksekusi fungsi
print(hasil_penjumlahan+10)

# Memanggil fungsi yang memiliki pernyataan return tanpa ekspresi
hasil_perkalian = perkalian(2, 5)

# Menampilkan hasil eksekusi fungsi
print(hasil_perkalian)