# Program untuk menghitung luas dan keliling persegi panjang

print('\n==========Program Hitung Luas dan Keliling Persegi Panjang==========\n')

panjang = float(input('Masukan panjang : '))
lebar = float(input('Masukan lebar   : '))

luas = panjang * lebar
keliling = (2 * panjang) + (2 * lebar)

print('\nLuas persegi panjang adalah', luas)
print('Keliling persegi panjang adalah', keliling, '\n') 