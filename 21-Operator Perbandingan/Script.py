# Operator perbandingan

print('\n==========Operator Perbandingan==========\n')

x = int(input('Masukan nilai x: '))
y = int(input('Masukan nilai y: '))

print('')
print(x, '==', y, ' =', (x==y))
print(x, '!=', y, ' =', (x!=y))
print(x, '<', y, '  =', (x<y))
print(x, '>', y, '  =', (x>y))
print(x, '<=', y, ' =', (x<=y))
print(x, '>=', y, ' =', (x>=y))
print('')