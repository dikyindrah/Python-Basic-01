# Operator aritmatika

print('\n==========Operator Aritmatika==========\n')

x = int(input('Masukan nilai x: '))
y = int(input('Masukan nilai y: '))

print('')
print(x, '+', y, '  =', (x+y))
print(x, '-', y, '  =', (x-y))
print(x, '*', y, '  =', (x*y))
print(x, '/', y, '  =', (x/y))
print(x, '//', y, ' =', (x//y))
print(x, '%', y, '  =', (x%y))
print(x, '**', y, ' =', (x**y))
print('')